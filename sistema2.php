		 <h3>Sistema Bibliotecario (SISBIBLI)</h3>
		 
			<p>	O Sistema Bibliotecario (SISBIBLI) é um aplicativo que permite a inclusão digital do cidadão na
				sociedade da informação. Trata-se de um software para catalogação, difusão e controle de acervos de bibliotecas
				públicas, desenvolvidos especialmente para atender escolas da rede pública de ensino de Porto Nacional- Tocantins.
				o sistema encontra-se em sua primeira versão dando suporte apenas a biblioteca de pequeno e medio porte. Além disso,
				o sistema possibilita a que o aluno também interaja com o sistema realizando consultas e visualizando seu historico de 
				emprestimo e sua situação na biblioteca. Assim possibilitando maior facilidade para o bibliotecario administrar o acervo 
				da biblioteca. </p>

			<p>	O sistema é livre como General Public Licence da Free Software Foundation (GPLv3) especifica em seus documentos, pode ser usado
				pode ser usado e modificado de acordo com as necessidades de cada instituição de ensino. </p>
				
			<p>	O sistema foi desenvolvido como projeto para conclusão do curso de Licenciatura em Computação do Instituto Federal de Ciência e 
				Tecnólogia do Tocantins(IFTO), Sob Orientação do Professor Mestre Rafael Miranda Correia. </p>

				
				<h3> Modulos do Sistema:</h3>
				
			<p>
				<ul type="square">
					<li>Cadastro, atualização e exclusão de Usuários;</li>
						 - Possibilita que todos os alunos da instituição sejam cadastrados em um banco de dados.
					<li>Cadastro, atualização e exclusão de Livros;</li>
						 - Possibilita que todo o acervo da instituição sejam cadastrados em um banco de dados.
					<li>Cadastro, atualização e exclusão de Administradores;</li>
						 - Possibilita que todos os responsavéis pela biblioteca da instituição sejam cadastrados em um banco de dados com acesso total ao sistema.
					<li>Cadastro, renovação e devolução de Emprestimo;</li>
						 - Todos os emprestimos ficam cadastratos no banco de dados com data de emprestimo e data de devolução.
					<li>Consulta a todos os modulos;</li>
						 - Todos os mudulos possui um sistema de busca para facilita a localização; 
					<li>Sistema de Login com Permissões;</li>
						 - Todos os Usuários possuem um nivel de permissão que faz o controle de acesso a áreas restritas do sistema. 
					<li>Consulta sem cadastro;</li>
						 - O usuário visitante do sistema pode fazer consulta ao acervo livremente sem possuir um cadastro no sistema.
					<li>Geração de Relatorios automáticos;</li> 
						 - O sistema faz geração automática de relatorios que possibilita maior facilidade do administrador.
					<li>Cadastro, atualização e exclusão de Noticias e eventos no sistema;</li>
						 - O sistema possui integrado outro sistema de cadastro e gerenciamento de noticias.
				</ul>		
			</p>
